package arch.math;

public interface Fractions {

    /**
     * Entero
     */
    int WHOLE = 1;

    /**
     * Medio
     */
    double MEDIUM = 0.5;

    /**
     * Tercio
     */
    double THIRD = 1 / 3;

    /**
     * Cuarto
     */
    double QUARTER = 0.25;

    /**
     * Quinto
     */
    double FIFTH = 1 / 5;

    /**
     * Sexto
     */
    double SIXTH = 1 / 6;

    /**
     * Séptimo
     */
    double SEVENTH = 1 / 7;

    /**
     * Octavo
     */
    double EIGHTH = 1 / 8;

    /**
     * Noveno
     */
    double NINTH = 1 / 9;

    /**
     * Décimo
     */
    double TENTH = 0.1;
}
